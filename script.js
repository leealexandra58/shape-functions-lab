function getAreaOfCircle(myRadius) {
    return (myRadius * myRadius * Math.PI);
}
console.log(getAreaOfCircle(5));


function getCircumferenceOfCircle(myRadius) {
    return (myRadius * 2 * Math.PI);
}
console.log(getCircumferenceOfCircle(10));


function getAreaOfSquare(side) {
    return (side * side);
}
console.log(getAreaOfSquare(15));


function getAreaOfTriangle(base, height) {
    return (.5 * base * height);
}
console.log(getAreaOfTriangle(20, 25));